function update(delta)
    frames = frames + 1;
    if player:getHull() < 10000 then
        player:setHull(10000);
        hits = hits + 1;
    end

    allKilled = true;
    for i,j in pairs(drones) do
        if j:isValid() then
            allKilled = false;
        end
    end

    if allKilled and not done then
        done = true;
        station:sendCommsMessage(player, "Vous avez complété la première partie de l'examen en " .. math.floor(frames / 60) .. " secondes et vous avez frappé " .. hits .. " mines. Veuillez aviser votre examinateur pour la suite.");
    end
end
